    public static void cadastraEvento() throws SQLException {
        String nomeEvento = txtNomeEvento.getText();
        String dataEvento = ftfDataEvento.getText();
        if (!nomeEvento.equals("") && !dataEvento.equals("")) {
            eventoDAO = new EventoDAO();
            eventoDAO.CadastrarEvento(con, new Evento(0, nomeEvento, dataEvento));
            LimparCampos.limpaCampos(jPanel1);
        } else {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        }
    }
