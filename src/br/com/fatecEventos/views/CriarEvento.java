    private void ftfDataEventoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ftfDataEventoKeyPressed
        tecla = evt.getKeyCode();
        if (tecla == KeyEvent.VK_ENTER) {
            try {
                cadastraEvento();
            } catch (Exception e) {

            }
        } else if (tecla != KeyEvent.VK_BACK_SPACE && tecla != KeyEvent.VK_ENTER) {
            int numeroData = ftfDataEvento.getText().length();
            novaData = ftfDataEvento.getText();
            if (numeroData == 2 || numeroData == 5) {
                novaData += "/";
                ftfDataEvento.setText(novaData);
            }
        }
    }//GEN-LAST:event_ftfDataEventoKeyPressed

