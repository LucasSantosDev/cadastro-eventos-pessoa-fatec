    private void ftfContribuicaoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_ftfContribuicaoFocusLost
        if (ftfContribuicao.getText().toString().equals("")) {
            ftfContribuicao.setText("0,00");
        } else {

            int caracteres = ftfContribuicao.getText().length();
            String aux = "";
            String novoValor = "";
            int cont = 0;
            int quantCaracteresContribuicao = 0;
            while (cont < caracteres) {
                aux = ftfContribuicao.getText().substring(cont, cont + 1);
                if (aux.equals(",")) {
                    quantCaracteresContribuicao = ftfContribuicao.getText().replace(",", "").length();
                    aux = "";
                } else if (aux.equals(".")) {
                    quantCaracteresContribuicao = ftfContribuicao.getText().replace(".", "").length();
                    aux = "";
                } else if (!(aux.equals(",") && aux.equals("."))) {
                    quantCaracteresContribuicao = ftfContribuicao.getText().length();
                    aux = aux;
                }
                novoValor += aux;
                cont++;
            }
            String formatacao = "";
            if (quantCaracteresContribuicao == 1) {
                formatacao = "0,0#";
            } else if (quantCaracteresContribuicao == 2) {
                formatacao = "0,##";
            } else if (quantCaracteresContribuicao == 3) {
                formatacao = "#,##";
            } else if (quantCaracteresContribuicao == 4) {
                formatacao = "##,##";
            } else if (quantCaracteresContribuicao == 5) {
                formatacao = "###,##";
            } else if (quantCaracteresContribuicao == 6) {
                formatacao = "#.###,##";
            } else if (quantCaracteresContribuicao == 7) {
                formatacao = "##.###,##";
            } else if (quantCaracteresContribuicao == 8) {
                formatacao = "###.###,##";
            } else if (quantCaracteresContribuicao == 9) {
                formatacao = "#.###.###,##";
            } else if (quantCaracteresContribuicao == 10) {
                formatacao = "##.###.###,##";
            } else if (quantCaracteresContribuicao == 11) {
                formatacao = "###.###.###,##";
            }
            ftfContribuicao.setText(format(formatacao, novoValor));
        }
    }//GEN-LAST:event_ftfContribuicaoFocusLost

