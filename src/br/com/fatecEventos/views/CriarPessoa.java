    public static void cadastraPessoa(String nmE) throws SQLException {
        eventoDAO = new EventoDAO();
        arrayEventos = eventoDAO.PesquisarComboBox(con, "SELECT * FROM tb_evento WHERE nm_evento = '" + nmE + "'");
        pessoaDAO = new PessoaDAO();
        pessoaDAO.CadastrarPessoa(con, new Pessoa(0, tfNome.getText(), tfEmail.getText(), ftfData.getText(), tfCurso.getText(), ftfContribuicao.getText(), cbTurno.getSelectedItem().toString(), Integer.parseInt(cbCiclo.getSelectedItem().toString()), ""));
        int idPessoa = pessoaDAO.PesquisarUltimaPessoa(con);
        int idEvento = 0;
        int cont = 0;
        while (arrayEventos.size() > cont) {
            idEvento = arrayEventos.get(cont).getCodigo();
            cont++;
        }
        itemDAO = new ItemDAO();
        itemDAO.CadastrarItem(con, new Item(0, idPessoa, idEvento));
    }

